def remove_unnecessary_character(document):
    # Xoá các ký tự không cần thiết
    document = re.sub(
        r"[^\s\wáàảãạăắằẳẵặâấầẩẫậéèẻẽẹêếềểễệóòỏõọôốồổỗộơớờởỡợíìỉĩịúùủũụưứừửữựýỳỷỹỵđ_]",
        " ",
        document,
    )
    # Xoá khoảng trắng thừa
    document = re.sub(r"\s+", " ", document).strip()
    return document


